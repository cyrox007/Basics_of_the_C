void print_car();

int main(int argc, char const *argv[])
{
    if (argv[1] == NULL) {
        printf("Run the program with the --help command to find out what parameters it supports\n");
    } else if (strcmp(argv[1], "--help") == 0) {
        puts("-a -- print animal");
        puts("-h -- print human");
        puts("-c -- print car");
    } else if (strcmp(argv[1], "-a") == 0) {
        print_animal();
    } else if (strcmp(argv[1], "-h") == 0) {
        print_human();
    } else if (strcmp(argv[1], "-c") == 0) {
        print_car();
    } else {
        printf("Error!! Invalid parameter...\n");
    }
    
    return 0;
}

